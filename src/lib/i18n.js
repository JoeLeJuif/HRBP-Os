function readStoredLang() {
  try {
    const v = localStorage.getItem(STORAGE_KEY);
    if (v && SUPPORTED_LANGS.includes(v)) return v;
  } catch {}
  return DEFAULT_LANG;
}

let currentLang = readStoredLang();
const listeners = new Set();

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang) || lang === currentLang) return;
  currentLang = lang;
  try { localStorage.setItem(STORAGE_KEY, lang); } catch {}
  listeners.forEach(fn => fn(lang));
}

export function t(key, lang = currentLang) {
  const dict = translations[lang] || translations[DEFAULT_LANG];
  return dict[key] ?? translations[DEFAULT_LANG][key] ?? key;
}

export function useT() {
  const [lang, setLangState] = useState(currentLang);
  useEffect(() => {
    const listener = (l) => setLangState(l);
    listeners.add(listener);
    if (currentLang !== lang) setLangState(currentLang);
    return () => { listeners.delete(listener); };
  }, []);
  return {
    lang,
    setLang,
    t: (key) => t(key, lang),
  };
}

// English-style 1-vs-many pluralization. Picks `<base>.one` for n===1, `<base>.other` otherwise.
// French chrome rendered through this falls back to French plural rules in the dict (singular vs plural).
export function plural(t, n, baseKey) {
  return t(`${baseKey}.${n === 1 ? "one" : "other"}`);
}
